#Requires -Version 5.1
<#
.SYNOPSIS
    Writes the MAP-9C IsoMetaGrid registration packet under .local/ only.
    Produces: map9c-isometagrid-registration-packet.json,
              map9c-isometagrid-registration-packet.md,
              MAP_9C_ISOMETAGRID_REGISTRATION_PACKET.md,
              MAP_9C_HUMAN_RUNTIME_STEPS.md,
              variants/<variant-id>/variant-manifest.json (one per layout hypothesis).
    Does not run Project Zomboid. Does not write to Steam, Workshop, or PZ folders.
    Does not copy third-party files. Does not claim playable export.
    Exits 0 on success, exits 1 on error.
.PARAMETER Output
    Output directory. Must be under .local/ in the repo.
#>

param(
    [string]$Output = ''
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$repoRoot  = Split-Path -Parent $scriptDir

if (-not $Output) {
    $Output = Join-Path $repoRoot '.local\map9c-isometagrid-registration-packet'
}

# .local/ guard
$localDir  = Join-Path $repoRoot '.local'
$absOutput = [System.IO.Path]::GetFullPath($Output)
$absLocal  = [System.IO.Path]::GetFullPath($localDir)
if (-not $absOutput.StartsWith($absLocal)) {
    Write-Error "prepare-build42-map9c-isometagrid-registration-packet: output must be under .local/ in the repo. Refused: $Output"
    exit 1
}

$forbidden = @('media\maps','media/maps','Steam','workshop','ProjectZomboid','C:\Program Files','D:\Program Files')
foreach ($seg in $forbidden) {
    if ($absOutput -like "*$seg*") {
        Write-Error "prepare-build42-map9c-isometagrid-registration-packet: forbidden path segment '$seg' in output: $Output"
        exit 1
    }
}

New-Item -ItemType Directory -Force -Path $Output | Out-Null
$variantsDir = Join-Path $Output 'variants'
New-Item -ItemType Directory -Force -Path $variantsDir | Out-Null

$generatedAt = (Get-Date).ToUniversalTime().ToString('yyyy-MM-ddTHH:mm:ssZ')

# Variant definitions
$variants = @(
    [ordered]@{
        variant_id          = 'variant-A'
        hypothesis          = 'H1-H2'
        folder_layout       = 'common/media/maps/PZMapForge'
        map_folder_name     = 'PZMapForge'
        description         = 'Current known layout — common/media/maps/PZMapForge. Map= token PZMapForge matches folder name.'
        rationale           = 'This is the layout generated by MAP-7L. Tests whether this layout is sufficient for IsoMetaGrid.'
        status              = 'probe_pending_human_runtime_test'
        human_runtime_tested = $false
        isometagrid_result  = 'unknown'
        playable_claim_allowed = $false
    },
    [ordered]@{
        variant_id          = 'variant-B'
        hypothesis          = 'H2'
        folder_layout       = 'common/media/maps/pzmapforge_build42_candidate_v4_001'
        map_folder_name     = 'pzmapforge_build42_candidate_v4_001'
        description         = 'Child map-id layout — folder name matches first Map= token value.'
        rationale           = 'Tests whether IsoMetaGrid requires the folder name to exactly match the first Map= entry.'
        status              = 'probe_pending_human_runtime_test'
        human_runtime_tested = $false
        isometagrid_result  = 'unknown'
        playable_claim_allowed = $false
    },
    [ordered]@{
        variant_id          = 'variant-C'
        hypothesis          = 'H4'
        folder_layout       = '42/media/maps/PZMapForge'
        map_folder_name     = 'PZMapForge'
        description         = 'Build 42 versioned layout — 42/media/maps/PZMapForge.'
        rationale           = 'Tests whether Build 42 scans from the 42/ versioned subtree instead of common/.'
        status              = 'probe_pending_human_runtime_test'
        human_runtime_tested = $false
        isometagrid_result  = 'unknown'
        playable_claim_allowed = $false
    },
    [ordered]@{
        variant_id          = 'variant-D'
        hypothesis          = 'H4-H2'
        folder_layout       = '42/media/maps/pzmapforge_build42_candidate_v4_001'
        map_folder_name     = 'pzmapforge_build42_candidate_v4_001'
        description         = 'Build 42 versioned + child map-id layout.'
        rationale           = 'Tests combination of versioned 42/ subtree and child map-id folder name.'
        status              = 'probe_pending_human_runtime_test'
        human_runtime_tested = $false
        isometagrid_result  = 'unknown'
        playable_claim_allowed = $false
    },
    [ordered]@{
        variant_id          = 'variant-E'
        hypothesis          = 'H1-legacy'
        folder_layout       = 'media/maps/PZMapForge'
        map_folder_name     = 'PZMapForge'
        description         = 'Legacy root layout — media/maps/PZMapForge (no common/ or 42/ prefix).'
        rationale           = 'Tests whether IsoMetaGrid still reads from legacy root media/maps/ in Build 42.'
        status              = 'probe_pending_human_runtime_test'
        human_runtime_tested = $false
        isometagrid_result  = 'unknown'
        playable_claim_allowed = $false
    }
)

# Write variant manifests
foreach ($v in $variants) {
    $vDir = Join-Path $variantsDir $v.variant_id
    New-Item -ItemType Directory -Force -Path $vDir | Out-Null
    $v | ConvertTo-Json -Depth 5 | Set-Content -Encoding UTF8 -Path (Join-Path $vDir 'variant-manifest.json')
}

$packet = [ordered]@{
    schema                                     = 'pzmapforge.map9c-isometagrid-registration-packet.v0.1'
    generated_at_utc                           = $generatedAt
    map9b_debug_runtime_evidence_recorded      = $true
    mod_load_confirmed                         = $true
    workshop_runtime_cache_confirmed           = $true
    spawn_metadata_works                       = $true
    isometagrid_map_folder_list_empty          = $true
    map_folder_registration_unproven           = $true
    playable_terrain_mount_proven              = $false
    canary_writer_available                    = $false
    canary_writer_blocked                      = $true
    current_server_map_line                    = 'Map=pzmapforge_build42_candidate_v4_001;PZMapForge;Muldraugh, KY'
    muldraugh_bootstrap_required               = $true
    no_muldraugh_strategy_rejected             = $true
    registration_probe_goal                    = 'make_PZMapForge_appear_in_IsoMetaGrid_map_folder_list'
    success_signal                             = 'PZMapForge_or_candidate_folder_appears_in_IsoMetaGrid_map_folder_list'
    failure_signal                             = 'IsoMetaGrid_map_folder_list_empty_or_only_vanilla'
    human_manual_runtime_test_required         = $true
    test_one_variant_at_a_time                 = $true
    stale_b41_server_console_ignored           = $true
    debug_build_required                       = $true
    fresh_world_required                       = $true
    layout_variants_defined                    = $variants.Count
    layout_variants                            = @($variants | ForEach-Object { $_.variant_id })
    layout_variant_folder_paths                = @($variants | ForEach-Object { $_.folder_layout })
    playable_claim_allowed                     = $false
    claude_ran_pz                              = $false
    claude_wrote_steam                         = $false
    claude_uploaded_workshop                   = $false
    steam_write_performed                      = $false
    workshop_upload_performed                  = $false
    pz_run_performed                           = $false
    third_party_files_copied                   = $false
    local_only                                 = $true
    recommended_next_branch                    = 'map9c_isometagrid_registration_probe_human_runtime_test_pending'
}

$jsonPath    = Join-Path $Output 'map9c-isometagrid-registration-packet.json'
$mdPath      = Join-Path $Output 'map9c-isometagrid-registration-packet.md'
$overlayDoc  = Join-Path $Output 'MAP_9C_ISOMETAGRID_REGISTRATION_PACKET.md'
$humanSteps  = Join-Path $Output 'MAP_9C_HUMAN_RUNTIME_STEPS.md'

$packet | ConvertTo-Json -Depth 10 | Set-Content -Encoding UTF8 -Path $jsonPath
Write-Output "MAP-9C packet JSON: $jsonPath"

$md = @"
