$($variants | ForEach-Object { "- $($_.variant_id): $($_.folder_layout)" } | Out-String)

## Safety

- claude_ran_pz: $($packet.claude_ran_pz)
- claude_wrote_steam: $($packet.claude_wrote_steam)
- third_party_files_copied: $($packet.third_party_files_copied)
- playable_claim_allowed: $($packet.playable_claim_allowed)
"@

$md | Set-Content -Encoding UTF8 -Path $mdPath
Write-Output "MAP-9C packet MD:   $mdPath"

