"@ -Encoding ASCII
Write-Output "Wrote: MAP_8L_WORLDMAP_XML_SUBSTANTIAL_CANDIDATE_PACKET.md"

Write-Output ""
Write-Output "MAP-8L candidate complete."
Write-Output "worldmap_xml_substantial: $xmlSubstantial"
Write-Output "worldmap_xml_size_bytes:  $xmlSizeBytes"
Write-Output "binary_contents_read:     false"
Write-Output "no_project_russia_content_used: true"
Write-Output "PUBLIC_PLAYABLE_CLAIM_ALLOWED=false"
Write-Output "Done."
